first = {1,2,3,4,5,6}
second = {4,5,6,7,8,9}

print(first | second) #todos los numeros ( unión |)
print(first & second)#los numeros repetidos (intersección &)
print(first - second)#los elementos en el primer cojunto pero no en el segundo ( diferencia -n)
print(first ^ second)#los que no se repiten (diferencia simetriac ^)